    private void JBNMGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBNMGuardarActionPerformed
        // TODO add your handling code here:
        
        if (JTNMNombreDeLaMateria.getText().isEmpty())
        {
            
            JOptionPane.showMessageDialog(null, "Por Favor Ingrese El Nombre De La Materia","Error",JOptionPane.ERROR_MESSAGE);                      
        }
        
        else if (JTNMProfesor.getText().isEmpty())
        {
            
                JOptionPane.showMessageDialog(null, "Por Favor Ingrese El Nombre Del Profesor","Error",JOptionPane.ERROR_MESSAGE);                         
        }
        if (JOptionPane.showConfirmDialog(null,"Desea Registrar La Materia","Confirmar",1)==0){
           NuevaMaterias.DatosDeIngreso leer = new NuevaMaterias.DatosDeIngreso();
            
           
            leer.setNombre(JTNMNombreDeLaMateria.getText());
            leer.setProfesor(JTNMProfesor.getText());
            leer.setFecha(JDNMFechaDeIngreso.getDate().toString());
            leer.setCurso(JCNMCurso.getSelectedItem().toString());
            
            //INGRESAR DATOS A TABLA
            DefaultTableModel modelo = (DefaultTableModel) JTNuevaMaterias.getModel();


            Object [] Fila=new Object[4];

            Fila[0]=JTNMNombreDeLaMateria.getText();
            Fila[1]=JTNMProfesor.getText();
            Fila[2]=JDNMFechaDeIngreso.getDate().toString();
            Fila[3]=JCNMCurso.getSelectedItem().toString();
            
            modelo.addRow(Fila);


            JTNuevaMaterias.setModel(modelo);
        
        this.guardar();
    }//GEN-LAST:event_JBNMGuardarActionPerformed
    }
