    public void guardar(){
        String nom=this.JTNMNombreDeLaMateria.getText();
        String pro=this.JTNMProfesor.getText();
        String fec= this.JDNMFechaDeIngreso.getDate().toString();
        String cur=this.JCNMCurso.getSelectedItem().toString();
        
        try {
            cts=cn.prepareCall("{call IngresarNuevaMateria(?,?,?,?)}");
            cts.setString(1, nom);
            cts.setString(2, pro);
            cts.setString(3, fec);
            cts.setString(4, cur);
                        
            int rpta=cts.executeUpdate();
            if (rpta==1)
            {
                JOptionPane.showMessageDialog(null, "Datos Ingresados Correctamente");
            }
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "No se guardo correctamente");
        }
    }
        
    

